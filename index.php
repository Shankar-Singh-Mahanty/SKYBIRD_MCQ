        <script>
            // JavaScript to toggle the menu visibility
            document
                .querySelector(".menu-toggle")
                .addEventListener("click", function () {
                    document
                        .querySelector("nav")
                        .classList.toggle("menu-active");
                });

            // Close the menu when any menu item is clicked
            document
                .querySelectorAll(".menu li a")
                .forEach(function (menuItem) {
                    menuItem.addEventListener("click", function () {
                        document
                            .querySelector("nav")
                            .classList.remove("menu-active");
                    });
                });

            // Close the menu if the user clicks anywhere outside the nav
            document.addEventListener("click", function (event) {
                const nav = document.querySelector("nav");
                const menu = nav.querySelector(".menu");
                const toggleButton = nav.querySelector(".menu-toggle");

                // Check if the click was outside the nav or the toggle button
                if (
                    !nav.contains(event.target) &&
                    !toggleButton.contains(event.target)
                ) {
                    nav.classList.remove("menu-active");
                }
            });
        </script>
